def cross_product(u, v):
    """Calcule le produit vectoriel de deux vecteurs 3D.

    Args:
        u (Vector): Premier vecteur.
        v (Vector): Deuxième vecteur.

    Returns:
        Vector: Produit vectoriel `u × v`.

    Raises:
        ValueError: Si les vecteurs ne sont pas en 3D.
    """
    return u.cross_product(v)
